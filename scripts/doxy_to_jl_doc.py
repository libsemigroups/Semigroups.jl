@cache
@accepts(str, str, str)
def is_public(thing: str, fn: str, params_t: str) -> bool:
    if is_namespace(thing):
        return True
    xml = get_xml(thing, fn, params_t)
    prot = xml.get("prot")
    return prot is not None and prot == "public"


