@accepts(str)
def to_jl_type(param_types: str) -> str:
    param_types = param_types.replace("const", "")
    param_types = param_types.replace("&", "")
    param_types = re.sub(param_types, "\bsize_t\b", "Int64")
    param_types = param_types.replace("uint8_t", "Int64")
    param_types = param_types.replace("bool", "Bool")
    param_types = param_types.replace("typename", "")
    param_types = param_types.replace("std::vector", "Vector")
    param_types = param_types.replace("<", "{")
    param_types = param_types.replace(">", "}")
    param_types = param_types.replace(" ", "")

    return param_types


def to_jl_sig(
    thing: str, fn_name: str, param_types: str, include_return_type: bool = False
) -> str:
    result = f"{fn_name}("
    result += ",".join(
        f"{name}::{to_jl_type(type_)}"
        for name, type_ in params_dict(thing, fn_name, param_types).items()
    )
    result += ")"
    if include_return_type:
        result += f"::{to_jl_type(return_type(thing, fn_name, param_types))}"
    return result


def to_jl_ref(thing: str, fn_name: str | None = None, param_types: str | None = None):
    if fn_name is None and param_types is None:
        sig, name = shortname(thing), shortname(thing)
    elif param_types is None:
        sig, name = shortname(fn_name), shortname(fn_name)
    else:
        sig, name = (
            to_jl_sig(thing, fn_name, param_types, include_return_type=False),
            shortname(fn_name),
        )

    return f"[`{name}`](@ref Semigroups.{sig})"


def to_jl_doc(thing: str, text: str) -> str:
    text = re.sub(r"\ba\s+const\s+reference\s+to\s+a\b", "a", text)
    text = re.sub(r"\ba\s+reference\s+to\s+a\b", "a", text)
    text = re.sub(r"\ba\s+const\s+reference\b", "", text)
    text = re.sub(r"\ba\s+reference\b", "", text)
    text = re.sub(r"\bvector\b", "`Vector`", text)
    text = re.sub(r"\buint8_t\b", "`Int64`", text)
    text = re.sub(f"{thing}", to_jl_ref(thing), text)
    return text


