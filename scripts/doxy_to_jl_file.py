# TODO rename
@accepts(str)
def to_jl_type(param_type: str) -> str:
    param_type = param_type.replace("libsemigroups::", "")
    param_type = re.sub(r"\bconst\b", "", param_type)
    param_type = re.sub(r"\bconstexpr\b", "", param_type)
    param_type = param_type.replace(r"&", "")
    param_type = re.sub(r"\bsize_t\b", "Int64", param_type)
    param_type = param_type.replace("uint8_t", "Int64")
    param_type = param_type.replace("bool", "Bool")
    param_type = param_type.replace("typename", "")
    param_type = param_type.replace("std::vector", "Vector")
    param_type = param_type.replace("<", "{")
    param_type = param_type.replace(">", "}")
    param_type = param_type.replace(" ", "")
    param_type = param_type.replace("void", "nothing")
    param_type = param_type.replace("uint64_t", "UInt64")
    param_type = param_type.replace("node_type", "Integer")
    param_type = param_type.replace("std::string", "String")

    return param_type


@accepts(str, str, str, bool)
def to_jl_sig(
    thing: str, fn_name: str, param_types: str, include_return_type: bool = False
) -> str:
    if is_mem_fn(thing, fn_name, param_types) and not is_constructor(thing, fn_name):
        params = {"self": f"{thing}"}
    else:
        params = dict()
    params = params | params_dict(thing, fn_name, param_types)
    result = f"{fn_name}("
    result += ",".join(f"{name}::{to_jl_type(type_)}" for name, type_ in params.items())
    result += ")"
    if include_return_type:
        result += f"::{to_jl_type(return_type(thing, fn_name, param_types))}"
    return result


# TODO accepts
def to_jl_ref(thing: str, fn_name: str | None = None, param_types: str | None = None):
    if fn_name is None and param_types is None:
        sig, name = shortname(thing), shortname(thing)
    elif param_types is None:
        sig, name = shortname(fn_name), shortname(fn_name)
    else:
        sig, name = (
            to_jl_sig(thing, fn_name, param_types, include_return_type=False),
            shortname(fn_name),
        )

    return f"[`{name}`](@ref Semigroups.{sig})"


# TODO rename this -> cxx_idioms_to_jl
def to_jl_doc(text: str) -> str:
    text = re.sub(r"\ba\s+const\s+reference\s+to\s+a\b", "a", text)
    text = re.sub(r"\ba\s+reference\s+to\s+a\b", "a", text)
    text = re.sub(r"\ba\s+const\s+reference\b", "", text)
    text = re.sub(r"\ba\s+reference\b", "", text)
    text = re.sub(r"\bconst\s+reference\b", "", text)
    text = re.sub(r"\breference\b", "", text)
    text = re.sub(r"\bvector\b", "`Vector`", text)
    text = re.sub(r"\bunsigned int\b", "integer", text)
    text = re.sub(r"`this`", "`self`", text)
    # text = re.sub(r"\bUNDEFINED\b", "[`UNDEFINED`](@ref)", text)
    return text


def convert_to_jl_doc(xml, context=[], params_dict: dict[str, str] = {}) -> str:
    context.append(xml.name)
    if "kind" in xml.attrs and xml.attrs["kind"] == "enum":
        context.append(xml.attrs["kind"])

    result = ""
    if xml.name == "compounddef":
        try:
            t = next((x for x in xml if x.name == "templateparamlist"))
            xml = [t] + [x for x in xml if x.name != "templateparamlist"]
        except StopIteration:
            pass
    if (
        not isinstance(xml, list)
        and "kind" in xml.attrs
        and xml.attrs["kind"] == "enum"
    ):
        n = next((x for x in xml if x.name == "name"))
        bd = ""
        try:
            bd = next((x for x in xml if x.name == "briefdescription"))
        except StopIteration:
            pass
        xml = [n, bd] + [x for x in xml if x.name not in ("briefdescription", "name")]
    for x in xml:
        if isinstance(x, str):
            x = x.strip()
            result += " " if x != "." and x != "" and not x[0].isupper() else ""
            result += x
        elif "enum" in context and x.name == "name":
            result += x.text.strip()
        elif "enum" in context and x.name == "enumvalue":
            # TODO convert from python to julia
            result += "\n\n.. py:enumerator:: "
            result += convert_to_jl_doc(x, context, params_dict)
        elif x.name == "briefdescription":
            result += (
                __wrap_text(convert_to_jl_doc(x, context, params_dict), width=80)
                + "\n\n"
            )
        elif x.name == "detaileddescription":
            result += "\n" + __wrap_text(
                convert_to_jl_doc(x, context, params_dict), width=80
            )
        elif x.name == "templateparamlist":
            params = []
            for y in x.find_all("param"):
                z = y.type.text
                if y.declname is not None:
                    z += " " + y.declname.text
                params.append(z)
            result += "template <" + ", ".join(params) + ">"
        elif x.name == "computeroutput":
            if len(x.text) != 0:
                result += f" `{x.text}`"
        elif x.name == "formula":
            result += f" ``{x.text.replace('$', '')}``"
        elif x.name == "title":
            result += f"\n\n# {x.text}\n\n"
        elif x.name == "para":
            result += convert_to_jl_doc(x, context, params_dict)
        elif x.name == "simplesect" and x.attrs["kind"] == "par":
            result += convert_to_jl_doc(x, context, params_dict)
        elif x.name == "parameterlist" and x.attrs["kind"] == "exception":
            for y in x.find_all("parameteritem"):
                exception = y.find("parametername").text
                if exception == "LibsemigroupsException":
                    exception = "LibsemigroupsError"
                result += "\n\n"
                if "Throws" not in result:
                    result += "# Throws"
                result += f"\n\n- {exception}: "
                result += convert_to_jl_doc(y.find("parameterdescription"), context)
        elif x.name == "parameterlist" and x.attrs["kind"] == "param":
            result += "\n\n# Arguments\n"
            paramitem = x.find_all("parameteritem")
            for y in paramitem:
                name = y.find("parametername").text
                if name in params_dict:
                    rtype = to_jl_type(params_dict[name])
                else:
                    rtype = "TODO"

                des = "\n\n".join(
                    des.text for des in y.find("parameterdescription").find_all("para")
                )
                result += f"\n - `{name}::{rtype}`: {des}\n"
        elif x.name == "simplesect" and x.attrs["kind"] == "see":
            result += "\n\n# See also\n\n-" + convert_to_jl_doc(x, context, params_dict)
        elif x.name == "ref":
            # TODO remove () from reference text
            text = x.text.replace("()", "")
            result += f" [`{text}`](@ref)"
        elif x.name == "emphasis":
            # TODO convert from python to julia
            result += f" *{x.text}*"
        elif x.name == "bold":
            # TODO convert from python to julia
            result += f"**{x.text}**"
        elif x.name == "compoundname":
            result += x.text[x.text.rfind("::") + 2 :]
        elif x.name == "ulink":
            # TODO convert from python to julia
            result += f"`{x.text} <{x.attrs['url']}>`_"
        elif x.name == "itemizedlist":
            result += "\n" + convert_to_jl_doc(x, context, params_dict)
        elif x.name == "listitem":
            result += "\n* " + convert_to_jl_doc(x, context, params_dict)
        elif x.name == "programlisting":
            # TODO convert from python to julia
            result += "\n\n.. code-block::\n" + convert_to_jl_doc(x)
        elif x.name == "codeline":
            result += "\n" + convert_to_jl_doc(x)
        elif x.name == "highlight":
            result += convert_to_jl_doc(x)
        elif x.name == "sp":
            result += " "

    if len(context) > 0 and context[-1] == "enum":
        context.pop()
    if context.pop() == "itemizedlist":
        result += "\n\n"

    return to_jl_doc(result)


