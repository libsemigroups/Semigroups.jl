@accepts(str, str, str)
def generate_func_doc(thing: str, fn: str, params_t: str) -> str:
    xml = get_xml(thing, fn, params_t)
    result = f"    {to_jl_sig(thing, fn, params_t, include_return_type=True)}\n\n"
    result += convert_to_jl_doc(xml, [], params_dict(thing, fn, params_t))

    result = re.sub(r"\s+,", ",", result)
    result = re.sub(r"\s+\.", ".", result)
    result = re.sub(
        r"# Exceptions\s*\n\nThis function is `noexcept` and is guaranteed never to throw.\s*\n?",
        "",
        result,
    )
    result = re.sub(
        r"# Exceptions\s*\n\nThis function guarantees not to throw.*\n?",
        "",
        result,
    )
    result = re.sub(r"\n{2,}", "\n\n", result)
    return result


@accepts(str, str, str)
def generate_func_decl(thing: str, fn: str, params_t: str) -> str:
    result = to_jl_sig(thing, fn, params_t, include_return_type=True)
    result += " = "
    if has_documented_throw(thing, fn, params_t):
        result += "@wrap_libsemigroups_call "

    result += "LibSemigroups."
    if not is_constructor(thing, fn):
        result += f"{to_jl_type(thing).lower()}_"
    result += f"{to_jl_type(fn)}("

    if is_mem_fn(thing, fn, params_t) and not is_constructor(thing, fn):
        params = ["self"]
    else:
        params = []
    params.extend(param_names_str(thing, fn, params_t))
    result += ", ".join(params) + ")\n"
    return result


@accepts(str, str, str)
def generate_func(thing: str, fn: str, params_t: str) -> str:
    return (
        '"""\n'
        + generate_func_doc(thing, fn, params_t)
        + '\n"""'
        + "\n"
        + generate_func_decl(thing, fn, params_t)
        + "\n"
    )


@accepts(str)
def generate(thing: str) -> str:
    if len(doxygen_filename(thing)) == 0:
        return ""

    result = copyright(thing)
    xml = get_xml(thing)
    for fn, sigs in sorted(xml.items()):
        for sig in sigs.keys():
            if not skip_fn(thing, fn, sig):
                result += generate_func(thing, fn, sig)
    return result


